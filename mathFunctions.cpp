#include "mathFunctions.h"
#include <cmath>

int absolute(int a){
    return abs(a);
}

double squareRoot(double a){
    return sqrt(a);
}

double reciprocalFunction(double a){
    return (1/a);
}

bool ascending(int a, int b) {
    return a>b;
}

bool descending(int a, int b) {
    return a<b;
}